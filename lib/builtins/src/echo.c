#include "builtins.h"

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	while (i < ac)
	{
		ft_putstr(av[i]);
		if (i < ac - 1)
			ft_putchar(' ');
		i++;
	}
	ft_putchar('\n');
	return (0);
}
